public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(education);

        String checkOut = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(checkOut);

        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        String backslash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
    }
}
